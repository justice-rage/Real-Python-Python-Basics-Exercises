# 4.3 Manipulate Strings With Methods

# 1) Write a program that converts the following strings to lowercase: 
# "Animals", "Badger", "Honey Bee", "Honey Badger". Print each lowercase
# string on a separate line.

a = "Animals"
b = "Badger"
hbe = "Honey Bee"
hbad = "Honey Badger"

print(a.lower())
print(b.lower())
print(hbe.lower())
print(hbad.lower())

# 2) Repeat exercise 1, but convert each string to uppercase instead of
# lowercase.

print(a.upper())
print(b.upper())
print(hbe.upper())
print(hbad.upper())

# 3) Write a program that removes whitespace from the following strings:
string1 = "   Filet Mignon"
string2 = "Brisket    "
string3 = "   Cheeseburger    "
# Print out the strings with the whitespace removed.

print(string1.lstrip())
print(string2.rstrip())
print(string3.strip())




